window.onload = function () {
    var container = document.querySelector("#container");
    var showimg = document.querySelector("#showing");
    var circles = 5;
    var clickFlag=true;
    var time;
    var index=0;
    var Distance=container.offsetWidth;

    function AutoGo(){
        var start=showimg.offsetLeft;
        var end=index*Distance*(-1);
        var change=end-start;
        var timer;
        var t=0;
        var maxT=10;
        clearInterval(timer);
        timer=setInterval(function(){
            t++;
            if(t>=maxT){
                clearInterval(timer);
                clickFlag=true;
            }
            showimg.style.left=change/maxT*t+start+"px";
            if(index==5&&t>=maxT){
                showimg.style.left=0;
                index=0;
            }
        },100);
    }
    function forward(){
        index++;
        if(index>5){
            index=0;
        }
        AutoGo();
    }
    function backward(){
        index--;
        if(index<0){
            index=4;
            showimg.style.left=(index+1)*Distance*(-1)+"px";
        }
        AutoGo();
    }
    time=setInterval(forward,3000);
    container.onmouseover=function(){
        clearInterval(time);
    }
    container.onmouseout=function(){
        time=setInterval(forward,3000); 
    }
}
